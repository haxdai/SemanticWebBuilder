    @Override
    public WizardPanelNavResult allowNext(String arg,Map map,Wizard wizard)
    {
        WizardPanelNavResult result=WizardPanelNavResult.REMAIN_ON_PAGE;
        if(this.jTreePages.getSelectionPath()==null)
        {            
            javax.swing.JOptionPane.showMessageDialog(null, "¡Debe seleccionar una página apublicarse!",getDescription(), JOptionPane.ERROR_MESSAGE);
            this.jTreePages.requestFocus();            
        }
        else
        {
            map.put("path", this.jTreePages.getSelectionPath());
            result=WizardPanelNavResult.PROCEED;
        }
        return result;
    }
    
